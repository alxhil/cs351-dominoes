package dominosConsole;

public class Domino {


    private int numOne;
    private int numTwo;


    public Domino (int one, int two) {
        this.numOne = one;
        this.numTwo = two;
    }


    public int getNumOne(){
        return this.numOne;
    }

    public int getNumTwo(){
        return this.numTwo;
    }

    public void setNumOne(int numOne) {this.numOne = numOne;}

    public void setNumTwo(int numTwo) {this.numTwo = numTwo;}







}
